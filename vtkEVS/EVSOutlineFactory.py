from vtkAtamai import OutlineFactory


class EVSOutlineFactory(OutlineFactory.OutlineFactory):

    """inherits from vtkAtamai.OutlineFactory and adds a few methods."""

    def __init__(self):
        OutlineFactory.OutlineFactory.__init__(self)

    def SetOpacity(self, opacity):
        """Set the opacity of the outline."""
        self._Property.SetOpacity(opacity)

    def GetOpacity(self):
        """Get the opacity of the outline."""
        return self._Property.GetOpacity()

    def ToggleOpacity(self):
        """Toggle the current opacity of the outline."""
        self._Property.SetOpacity(1 - self.GetOpacity())
        self.Modified()
        self.Render()
